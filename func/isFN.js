var toString = Object.prototype.toString;



/**
 * @Description: 判断当前链接是不是图片链接
 * @param {str} url 文件链接
 * @return: {bool} 是否正确  
 * @case:  isUrlImg(‘.../xx.png’) >true
 * ---------
 * @Author: yijian.song
 * @Date: 2019-05-05 15:46:21
 */
function isUrlImg(url){
  const path = require('path');
  if (typeof url !== 'string') throw new Error('isUrlImg 参数类型错误')
  let fil = path.extname(url)
  if (fil){
    let type = new Set(['.JPEG', '.JPG', '.GIF', '.PNG', '.APNG', '.SVG', '.BMP', '.PNG', '.ICO'])
    console.log(fil, type.has(fil.toUpperCase()))
    return type.has(fil.toUpperCase())
  }else{
    return url.indexOf('data:image/') === 0 && url.indexOf(';base64,') >11
  }
}

/**
 * Determine
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is an Array, otherwise false
 */
function isArray(val) {
  return toString.call(val) === '[object Array]';
}

/**
 * Determine
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is an ArrayBuffer, otherwise false
 */
function isArrayBuffer(val) {
  return toString.call(val) === '[object ArrayBuffer]';
}

/**
 * Determine
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is an FormData, otherwise false
 */
function isFormData(val) {
  return (typeof FormData !== 'undefined') && (val instanceof FormData);
}

/**
 * Determine
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is a view on an ArrayBuffer, otherwise false
 */
function isArrayBufferView(val) {
  var result;
  if ((typeof ArrayBuffer !== 'undefined') && (ArrayBuffer.isView)) {
    result = ArrayBuffer.isView(val);
  } else {
    result = (val) && (val.buffer) && (val.buffer instanceof ArrayBuffer);
  }
  return result;
}

